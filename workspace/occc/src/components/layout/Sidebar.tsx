export default function Sidebar() {
  const pathname = usePathname();
  const [pendingCount, setPendingCount] = useState(0);
  const [projectId, setProjectId] = useState<string | null>(null);

  useEffect(() => {
    if (typeof window !== 'undefined') {
      setProjectId(localStorage.getItem('occc-project'));
    }
  }, []);

  useEffect(() => {
    if (!projectId) return;
    const fetchCount = async () => {
      try {
        const res = await fetch(`/api/suggestions?project=${projectId}`);
        const data = await res.json();
        const count = (data.suggestions || []).filter((s: { status: string }) => s.status === 'pending').length;
        setPendingCount(count);
      } catch {
        // Silently ignore — badge is non-critical
      }
    };
    fetchCount();
    const interval = setInterval(fetchCount, 30000); // 30s poll
    return () => clearInterval(interval);
  }, [projectId]);

  return (
    <aside className="w-56 border-r border-gray-200 dark:border-gray-700 bg-white dark:bg-gray-800 flex flex-col">
      <div className="p-4 border-b border-gray-200 dark:border-gray-700">
        <h1 className="text-lg font-bold text-gray-900 dark:text-white tracking-tight">
          OCCC
        </h1>
        <p className="text-xs text-gray-500 dark:text-gray-400">Control Center</p>
      </div>

      <nav className="flex-1 p-3 space-y-1">
        {navItems.map(item => {
          const isActive = pathname === item.href || pathname?.startsWith(item.href + '/');
          const isSuggestions = item.href === '/suggestions';
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors ${
                isActive
                  ? 'bg-blue-50 dark:bg-blue-900/30 text-blue-700 dark:text-blue-300'
                  : 'text-gray-600 dark:text-gray-400 hover:bg-gray-50 dark:hover:bg-gray-700/50 hover:text-gray-900 dark:hover:text-gray-200'
              }`}
            >
              <div className="relative">
                {item.icon}
                {isSuggestions && pendingCount > 0 && (
                  <span className="absolute -top-1 -right-1 flex h-4 w-4 items-center justify-center rounded-full bg-red-500 text-[10px] font-bold text-white">
                    {pendingCount > 9 ? '9+' : pendingCount}
                  </span>
                )}
              </div>
              {item.label}
            </Link>
          );
        })}
      </nav>
    </aside>
  );
}
