# export_draft
# ---------------------------------------------------------------------------

def export_draft(proposal: TopologyProposal, project_id: str) -> Path:
    """
    Export a proposal's topology to proposal-draft.json for hard correction.

    Writes the topology graph dict with human-readable __comment__ annotations
    to help users understand what to edit. The comment keys are stripped on
    import_draft().

    Args:
        proposal: The TopologyProposal to export.
        project_id: The project identifier (determines output directory).

    Returns:
        Path to the written proposal-draft.json file.
    """
    topo_dir = _topology_dir(project_id)
    draft_path = topo_dir / "proposal-draft.json"

    # Build the topology dict
    data = proposal.graph.to_dict()

    # Add human-readable comment annotations
    data["__comment__nodes"] = (
        "Nodes: each has id (string), level (1/2/3), intent (what role does), "
        "risk_level (low/medium/high)"
    )
    data["__comment__edges"] = (
        "Edges: from_role/to_role (node ids), edge_type: "
        "delegation | coordination | review_gate | escalation | information_flow"
    )

    with open(draft_path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=2)

    logger.debug("Exported draft topology to %s", draft_path)
    return draft_path


# ---------------------------------------------------------------------------
# import_draft
# ---------------------------------------------------------------------------

def import_draft(
    project_id: str,
    registry: Any,
    max_concurrent: int,
) -> tuple:
    """
    Import and validate a manually-edited proposal-draft.json.

    Strips __comment__ keys, deserializes to TopologyGraph, and runs the
    constraint linter. Raises a clear ValueError on invalid edge_type values
    listing all valid options.

    Args:
        project_id: The project identifier (determines file location).
        registry: AgentRegistry for constraint linting.
        max_concurrent: Max concurrent L3 agents for pool constraint check.

    Returns:
        (TopologyGraph, LintResult) tuple.

    Raises:
        FileNotFoundError: If proposal-draft.json does not exist.
        ValueError: If edge_type values are invalid, with valid types listed.
    """
    topo_dir = _topology_dir(project_id)
    draft_path = topo_dir / "proposal-draft.json"

    if not draft_path.exists():
        raise FileNotFoundError(
            f"No proposal-draft.json found at {draft_path}. "
            "Run 'export_draft' first to create the draft file."
        )

    with open(draft_path, "r", encoding="utf-8") as f:
        raw = json.load(f)

    # Strip __comment__ keys
    clean = {k: v for k, v in raw.items() if not k.startswith("__comment__")}

    # Deserialize to TopologyGraph — catch invalid edge_type values with clear error
    valid_edge_types = [e.value for e in EdgeType]
    try:
        graph = TopologyGraph.from_dict(clean)
    except ValueError as exc:
        raise ValueError(
            f"Invalid edge_type in proposal-draft.json: {exc}. "
            f"Valid EdgeType values are: {', '.join(valid_edge_types)}"
        ) from exc

    # Build linter-compatible proposal dict
    lint_proposal_dict = {
        "roles": [
            {
                "id": n.id,
                "level": n.level,
                "intent": n.intent,
                "risk_level": n.risk_level,
            }
            for n in graph.nodes
        ],
        "edges": [
            {
                "from": e.from_role,
                "to": e.to_role,
                "type": e.edge_type.value,
            }
            for e in graph.edges
        ],
    }

    # Run constraint linter
    linter = ConstraintLinter(registry=registry, max_concurrent=max_concurrent)
    lint_result = linter.lint("hard_correction", lint_proposal_dict)

    logger.debug(
        "Imported draft topology for project=%s (valid=%s, adjusted=%s)",
        project_id,
        lint_result.valid,
        lint_result.adjusted,
    )

    return graph, lint_result
